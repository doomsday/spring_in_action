public class Computer {
  private short code;
  private short model;
  private short speed;
  private short ram;
  private short hd;
  private float price;
  private short screen;

  public short getCode() {
    return code;
  }

  public void setCode(short code) {
    this.code = code;
  }

  public short getModel() {
    return model;
  }

  public void setModel(short model) {
    this.model = model;
  }

  public short getSpeed() {
    return speed;
  }

  public void setSpeed(short speed) {
    this.speed = speed;
  }

  public short getRam() {
    return ram;
  }

  public void setRam(short ram) {
    this.ram = ram;
  }

  public short getHd() {
    return hd;
  }

  public void setHd(short hd) {
    this.hd = hd;
  }

  public float getPrice() {
    return price;
  }

  public void setPrice(float price) {
    this.price = price;
  }

  public short getScreen() {
    return screen;
  }

  public void setScreen(short screen) {
    this.screen = screen;
  }
}
